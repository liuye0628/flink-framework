package com.atguigu.javase.c_oop;
//访问修饰符
public class $08_AccessModifier {
    public static void main(String[] args) {
        /**
         * 基本介绍:java提供四种访问控制修饰符号,用于控制方法和属性的访问权限
         *  1.public:公开级别,对外公开
         *  2.protected:受保护级别,对子类和同一个包中的类公开
         *  3.默认:没有修饰符号,向同一个包中的类公开
         *  4.private:私有级别只有类本身可以访问,不对外公开
         *  private<<默认<<protected<<public
         *  编写代码时,建议成员变量使用private,构造方法和成员方法使用public
         */
    }
}
